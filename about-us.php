<script>
    document.querySelectorAll(".tab-nav-custom .texthovercol").forEach(tab => {
        tab.addEventListener("mouseenter", function() {
            // Remove 'hoverselected' class from all tab buttons and content rows
            document.querySelectorAll(".tab-nav-custom .texthovercol").forEach(el => el.classList.remove("hoverselected"));
            document.querySelectorAll(".tabimgrow").forEach(row => row.classList.remove("hoverselected"));

            // Add 'hoverselected' to the hovered tab
            this.classList.add("hoverselected");

            // Get the data-id and activate the corresponding image/content block
            const id = this.getAttribute("data-id");
            const target = document.querySelector(`.${id}`);
            if (target) {
                target.classList.add("hoverselected");
            }
        });
    });
</script>